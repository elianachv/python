# Determined loops : for
# range(n) could be a list, a tuple, a string, etc

print("Printing 5 times Hello world")
for i in range(5):
    print("Hello world!")

print("\n")

print("Printing names in a list")
for name in ["Isaboth", "Ichimei", "Eli"]:
    print("Hello", name)
